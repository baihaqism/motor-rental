    private void create(){
        try{
            if(!inputNamaPelanggan.getText().isEmpty() && !inputAlamat.getText().isEmpty() && !inputNoTelepon.getText().isEmpty() && !inputNoIdentitas.getText().isEmpty()){
                String kolom[] = {"nama_pelanggan","alamat","no_telp","no_identitas"};
                String isi[] = {inputNamaPelanggan.getText(),inputAlamat.getText(),inputNoTelepon.getText(),inputNoIdentitas.getText()};
                
                System.out.println( con.queryInsert("pelanggan",kolom,isi));
                
                JOptionPane.showMessageDialog(this, "Pelanggan Berhasil ditambahkan");
            } else {
                JOptionPane.showMessageDialog(this, "Data Pelanggan Masih ada yang Kosong");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Error input data");
            System.out.println("salah");
        }
        loadTabel();
        clear();
    }
