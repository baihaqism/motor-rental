    private void btnHapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHapusMouseClicked
        try{
            String id=String.valueOf(tblPelanggan.getValueAt(tblPelanggan.getSelectedRow(), 0));
            if(JOptionPane.showConfirmDialog(this, "Yakin menghapus data?", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION){
                con.queryDelete("pelanggan","id="+id);
            } else {
                return;
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "Pilih id terlebih dahulu");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_btnHapusMouseClicked

    private void btnUbahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUbahMouseClicked
        try{
            if( !inputIdPelanggan.getText().isEmpty() && !inputNamaPelanggan.getText().isEmpty() && !inputAlamat.getText().isEmpty() && !inputNoTelepon.getText().isEmpty() && !inputNoIdentitas.getText().isEmpty()){
                String kolom[] = {"id","nama_pelanggan","alamat","no_telp","no_identitas"};
                String isi[] = {inputIdPelanggan.getText(),inputNamaPelanggan.getText(),inputAlamat.getText(),inputNoTelepon.getText(),inputNoIdentitas.getText()};

                con.queryUpdate("pelanggan", kolom, isi, "id='"+String.valueOf(tblPelanggan.getValueAt(tblPelanggan.getSelectedRow(),0))+"'");
                JOptionPane.showMessageDialog(this, "Data Berhasil Diedit");
            } else {
                JOptionPane.showMessageDialog(this, "Data isina ada yang kosong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error edit data");
        }
        loadTabel();
        clear();
    }//GEN-LAST:event_btnUbahMouseClicked

